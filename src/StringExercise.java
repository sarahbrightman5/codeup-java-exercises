public class StringExercise {
    public static void main(String[] args) {
        String pf = "We don't need no education\n" +
                "We don't need no thought control\n";
        System.out.println(pf.trim());
        String var2 = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(var2);
        String var3 = "In windows, the main drive is usually C:\\\n";
        System.out.println(var3);
        String var4 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(var4);

    }

}
